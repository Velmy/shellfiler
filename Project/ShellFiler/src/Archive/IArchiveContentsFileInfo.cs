using System;
using System.Collections.Generic;
using System.Text;
using System.Drawing;
using System.Windows.Forms;
using ShellFiler.Api;
using ShellFiler.Document;
using ShellFiler.FileSystem;
using ShellFiler.Util;
using ShellFiler.UI;

namespace ShellFiler.Archive {

    //=========================================================================================
    // クラス：アーカイブ内のファイル情報
    //=========================================================================================
    public interface IArchiveContentsFileInfo {

        //=========================================================================================
        // 機　能：破棄する
        // 引　数：なし
        // 戻り値：なし
        //=========================================================================================
        void Dispose();

        //=========================================================================================
        // プロパティ：アーカイブ内のインデックス
        //=========================================================================================
        int Index {
            get;
        }

        //=========================================================================================
        // プロパティ：アーカイブ内のファイル名（ファイル名が決められないときnull）
        //=========================================================================================
        string FileName {
            get;
            set;
        }
        
        //=========================================================================================
        // プロパティ：アーカイブ内のフルパスファイル名
        //=========================================================================================
        string FilePath {
            get;
        }
        
        //=========================================================================================
        // プロパティ：ディレクトリのときtrue
        //=========================================================================================
        bool IsDirectory {
            get;
        } 
       
        //=========================================================================================
        // プロパティ：最終更新日時（取得できないときDateTime.MinValue）
        //=========================================================================================
        DateTime LastWriteTime {
            get;
        }        

        //=========================================================================================
        // プロパティ：ファイルサイズ（取得できないとき-1）
        //=========================================================================================
        long FileSize {
            get;
        }

        //=========================================================================================
        // プロパティ：処理結果のステータス
        //=========================================================================================
        FileOperationStatus ResultStatus {
            get;
        }
    }
}
