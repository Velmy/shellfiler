    //=========================================================================================
    // インターフェース：オプションダイアログの各ページが実装するインターフェース
    //=========================================================================================
    public interface IOptionDialogPage {
        
        //=========================================================================================
        // 機　能：フォームが閉じられたときの処理を行う
        // 引　数：なし
        // 戻り値：なし
        //=========================================================================================
        void OnFormClosed();

        //=========================================================================================
        // 機　能：UIから設定値を取得する
