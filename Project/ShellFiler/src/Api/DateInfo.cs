using System;
using System.Collections.Generic;
using System.IO;
using System.Text;

namespace ShellFiler.Api {

    //=========================================================================================
    // クラス：日付情報
    //=========================================================================================
    public class DateInfo {
        // 年（YYYY）
        private int m_year;
        
        // 月（MM）
        private int m_month;
        
        // 日（DD）
        private int m_day;
        
        //=========================================================================================
        // 機　能：コンストラクタ
        // 引　数：なし
        // 戻り値：なし
        //=========================================================================================
        public DateInfo() {
        }

        //=========================================================================================
        // 機　能：コンストラクタ
        // 引　数：[in]year   年（YYYY）
        // 　　　　[in]month  月（MM）
        // 　　　　[in]day    日（DD）
        // 戻り値：なし
        //=========================================================================================
        public DateInfo(int year, int month, int day) {
            m_year = year;
            m_month = month;
            m_day = day;
        }

        //=========================================================================================
        // プロパティ：年（YYYY）
        //=========================================================================================
        public int Year {
            get {
                return m_year;
            }
            set {
                m_year = value;
            }
        }
        
        //=========================================================================================
        // プロパティ：月（MM）
        //=========================================================================================
        public int Month {
            get {
                return m_month;
            }
            set {
                m_month = value;
            }
        }
        
        //=========================================================================================
        // プロパティ：日（DD）
        //=========================================================================================
        public int Day {
            get {
                return m_day;
            }
            set {
                m_day = value;
            }
        }
    }
}
